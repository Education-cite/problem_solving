package hakerRankProblemSolving;

/*Question: in this challenge,you must read 3 integers from stdin and print them stdout
 Each integer must be printed on a new line.
 
 */

import java.util.Scanner;

public class String_Solution2 {
	public static void main(String[] args) {
		Scanner s = new Scanner(System.in);
		
		System.out.println("Please input first number");
		int a = s.nextInt();
		
		System.out.println("Please input 2nd number");
		int b = s.nextInt();
		
		System.out.println("Please input 3rd number");
		int c = s.nextInt();
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);


				
	}

}
